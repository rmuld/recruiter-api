package com.recruiter.recruiterapi;

public class Education {
    private String education_level;
    private String school_name;
    private String subject;
    private String study_begin;
    private String study_end;

    public String getEducation_level() { return education_level; }
    public void setEducation_level(String education_level) { this.education_level = education_level; }

    public String getSchool_name() { return school_name; }
    public void setSchool_name(String school_name) { this.school_name = school_name; }

    public String getSubject() { return subject; }
    public void setSubject(String subject) { this.subject = subject; }

    public String getStudy_begin() { return study_begin; }
    public void setStudy_begin(String study_begin) { this.study_begin = study_begin; }

    public String getStudy_end() { return study_end; }
    public void setStudy_end(String study_end) { this.study_end = study_end; }


}
